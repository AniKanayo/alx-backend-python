#!/usr/bin/env python3


def floor(n: float) -> int:
    """
    Return the floor of a float.

    Args:
        n (float): The input float.

    Returns:
        int: The floor value of the input.

    Example:
        >>> floor(3.14)
        3
    """
    return int(n)


print(floor(3.14))  # Output: 3
